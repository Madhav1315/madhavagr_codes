public class divisibility {
    public static void main(String[] args) {
        int a=17;
        if(a%3==0){
            System.out.println("a is divisible by 3");
        }
        if(a%5==0){
            System.out.println("a is divisible by 5");
        }
        else{
            System.out.println("a is neither divisible by 3 nor by 5");
        }
    }
}
